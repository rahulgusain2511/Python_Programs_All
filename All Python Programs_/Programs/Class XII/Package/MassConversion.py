'''This module help us to convert the mass into given quantity. '''
def kgtotonne(a):
    '''This function convert the mass from kg to tonne'''
    return a/1000

def tonnetokg(a):
    '''This function convert the mass from tonne to kg'''
    return a*1000

def kgtopound(a):
    '''This function convert the mass from kg to pound'''
    return a*2.20462

def poundtokg(a):
    '''This function convert the mass from pound to kg'''
    return a/2.20462


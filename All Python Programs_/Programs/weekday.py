#weekday
n = int(input("Enter any number (1-7): "))
if n==1:
    print("MONDAY")
elif n==2:
    print("TUESDAY")
elif n==3:
    print("WEDNESDAY")
elif n==4:
    print('THURSDAY')
elif n==5:
    print('FRIDAY')
elif n==6:
    print('SATURDAY')
elif n==7:
    print('SUNDAY')
else:
    print('Wrong number entered')
